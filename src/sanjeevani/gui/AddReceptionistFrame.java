    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        if(validateInputs()==false){
            JOptionPane.showMessageDialog(null,"Please enter all data");
            return;
        }
        String passwordCheck=matchPassword();
        if(passwordCheck!=null){
            JOptionPane.showMessageDialog(null,passwordCheck);
            return;
        }

        try{
            ReceptionistPojo rec=new ReceptionistPojo(this.txtReceptionistId.getText(),selEmpName,jcGender.getSelectedItem().toString());
            boolean result1=ReceptionistDao.addReceptionist(rec);
            UserPojo user=new UserPojo();
            user.setLoginId(loginId);
            user.setUserName(selEmpName);
            user.setUserType("RECEPTIONIST".toLowerCase());
            user.setPassword(PasswordEncryption.getEncryptedPassword(password));
            boolean result2=UserDao.addUser(user);
            if(result1==true && result2==true){
                JOptionPane.showMessageDialog(null, "Receptionist Added Successfully!");

                clearAll();
                return;
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error in DB"+e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

