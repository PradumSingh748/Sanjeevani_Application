    private boolean validateInputs(){
        loginId=txtLoginId.getText().trim();
        char[]pwd1=txtPassword.getPassword();
        char[]pwd2=txtRePassword.getPassword();
        
       if(loginId.isEmpty()||pwd1.length==0||pwd2.length==0){
           return false;
        }
       password=new String(pwd1).trim();
       repassword=new String(pwd2).trim();
       return true;
    }

    private String matchPassword() {
        if(password.length()<3||repassword.length()<3){
            return "Password must be of atleast 3 digits";
    }
        if(password.equals(repassword)==false){
            return "Passwords do not match";
        }
        return null;
    }

    private void clearAll() {
        txtLoginId.setText("");
        txtPassword.setText("");
        txtRePassword.setText("");
        txtReceptionistName.setText("");
        unRegRecList.remove(selEmpName);
        jcEmployeeId.removeItem(selEmpId);
        if(jcEmployeeId.getItemCount()==0){
            JOptionPane.showMessageDialog(null,"All Receptionist registered!");
            JFrame fr=new ManageReceptionistFrame();
            fr.setVisible(true);
            this.dispose();
        }
        
    }
