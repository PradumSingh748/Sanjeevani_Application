  private void loadRecDteails() {
      try{
          unRegRecList=EmpDao.getUnRegRecIdList();
          loginId=txtLoginId.getText().trim();
          Set<String> recId=unRegRecList.keySet();
          for(String id:recId){
              jcEmployeeId.addItem(id);
          }
          if(jcEmployeeId.getItemCount()==0){
              JFrame f=new ManageReceptionistFrame();
              f.setVisible(true);
              this.dispose();
          }
          else{
              String name=this.unRegRecList.get(jcEmployeeId.getSelectedItem().toString());
//        txtReceptionistName.setText(name);
        System.out.println("Name "+name);
          }
      }
      catch(SQLException e){
          JOptionPane.showMessageDialog(null, "Error in DB!"+e.getMessage());
      }
    }

