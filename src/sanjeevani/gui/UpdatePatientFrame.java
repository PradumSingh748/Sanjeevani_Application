    private void jcDoctorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcDoctorIdActionPerformed
        // TODO add your handling code here:
        String selDocId=this.jcDoctorId.getSelectedItem().toString();
        try{
            patients=PatientDao.getAllPatientDetails();
            for(PatientPojo p:patients){
                if(p.getDoctorId().equalsIgnoreCase(selDocId)){
                    this.jcPatId.addItem(p.getPatient_id());
                }
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error in DB!"+e.getMessage());
        }
    }//GEN-LAST:event_jcDoctorIdActionPerformed

