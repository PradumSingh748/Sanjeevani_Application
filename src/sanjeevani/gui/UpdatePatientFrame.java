    private void jcPatIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcPatIdActionPerformed
        // TODO add your handling code here:

        for(PatientPojo p:patients){
            if(p.getPatient_id().equalsIgnoreCase(jcPatId.getSelectedItem().toString())){
                //                this.jcPatId.addItem(p.getPatient_id());
                this.txtOPD.setText(p.getOpd());
                this.txtAge.setText(String.valueOf(p.getAge()));
                this.txtCity.setText(p.getCity());
                this.jtAddress.setText(p.getAddress());
                this.txtFirstName.setText(p.getFirstName());
                this.txtLastName.setText(p.getLastName());
                this.txtDate.setText(String.valueOf(p.getpDate()));
                this.txtPhoneNo.setText(p.getPhoneNo());
                this.jcGender.setSelectedItem(p.getGender());
                this.jcMStatus.setSelectedItem(p.getmStatus());
            }
        }

    }//GEN-LAST:event_jcPatIdActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PatientPojo patient=new PatientPojo();
        patient.setAddress(this.jtAddress.getText());
        patient.setAge(Integer.parseInt(this.txtAge.getText()));
        patient.setCity(this.txtCity.getText());
        patient.setDoctorId(this.jcDoctorId.getSelectedItem().toString());
        patient.setFirstName(this.txtFirstName.getText());
        patient.setLastName(this.txtLastName.getText());
        patient.setGender(this.jcGender.getSelectedItem().toString());
        patient.setOpd(this.txtOPD.getText());
        patient.setPatient_id(this.jcPatId.getSelectedItem().toString());
        patient.setPhoneNo(this.txtPhoneNo.getText());
        patient.setmStatus(this.jcMStatus.getSelectedItem().toString());
        AppointmentPojo app=new AppointmentPojo();
        app.setMobile_no(txtPhoneNo.getText());
        app.setOpd(txtOPD.getText());
        app.setDate_time(txtDate.getText());
        app.setPatient_name(txtFirstName.getText()+" "+txtLastName.getText());
        app.setPatient_id(patient.getPatient_id());
        try{
            app.setDoctor_name(DoctorDao.getDoctorNameByDoctorId(patient.getDoctorId()));
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error in DB!"+e.getMessage());
        }
        Date d;
        java.sql.Date dt;
        try{
            String date=this.txtDate.getText();
            SimpleDateFormat sdf=new SimpleDateFormat("dd-MM-YYYY");

            d=sdf.parse(date);
            long ms=d.getTime();
            dt=new java.sql.Date(ms);
            patient.setpDate(dt);
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,"error in date formatting"+ex.getMessage());
        }

        try{
            boolean isUpdated=PatientDao.updatePatient(patient);
            boolean isAppointmentUpdated=AppointmentDao.updatePatientDetails(app);

            if(isUpdated==true && isAppointmentUpdated==true){
                JOptionPane.showMessageDialog( null,"Patient record updated successfully!");
            }
            else{
                JOptionPane.showMessageDialog(null,"Patient record cannot be updated!");
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error in DB!"+e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

