    private void jcReceptionistIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcReceptionistIdActionPerformed
        // TODO add your handling code here:
        try{

            rec=ReceptionistDao.getReceptionistDetails(jcReceptionistId.getSelectedItem().toString());
            txtReceptionistName.setText(rec.getName());
            oldname=rec.getName();
            jcGender.setSelectedItem(rec.getGender());
            System.out.println("name of the receptinsit"+ rec.getName());

            user=UserDao.getUserDetails(rec.getName());

            this.txtLoginId.setText(user.getLoginId());
            System.out.println("Encoded password=========>"+user.getPassword());
            String abc=PasswordDecryption.DecryptPassword(user.getPassword());
            System.out.println("password=============>"+abc);
            this.jPassword.setText(abc);

            this.jRePassword.setText(abc);

            rec.setGender(jcGender.getSelectedItem().toString());

        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error in DB"+e.getMessage());
        }
    }//GEN-LAST:event_jcReceptionistIdActionPerformed

