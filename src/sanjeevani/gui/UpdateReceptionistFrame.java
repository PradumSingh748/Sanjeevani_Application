    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            rec.setName(txtReceptionistName.getText());
            user.setLoginId(txtLoginId.getText());
            String enpwd=PasswordEncryption.getEncryptedPassword(this.jPassword.getText());
            user.setPassword(enpwd);
            user.setUserName(txtReceptionistName.getText());
            //             user.setUserType(oldname);
            boolean isReceptionistUpdated=ReceptionistDao.updateReceptionist(rec);
            boolean isUserUpdated=UserDao.updateUser(user, oldname);
            boolean isEmpUpdated=EmpDao.updateEmployeeName(oldname,txtReceptionistName.getText().toString());

            if(isReceptionistUpdated == true && isUserUpdated== true && isEmpUpdated==true){
                JOptionPane.showMessageDialog(null,"Updates successfully!");
                this.dispose();
                new ManageReceptionistFrame().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null,"Cannot be updated");
            }
            System.out.println("value of boolean updated" + isReceptionistUpdated);
            System.out.println("value of boolean updated" + isUserUpdated);
            System.out.println("value of emp upadted "+isEmpUpdated);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error in DB!"+e.getMessage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

